// Form Validation and Submit Logic
const contactForm = document.getElementById('contactForm');
if (contactForm) {
  contactForm.addEventListener('submit', function(e) {
    e.preventDefault();
    
    // Clear previous errors
    const errorMessages = document.querySelectorAll('.error-message');
    errorMessages.forEach(msg => {
      msg.classList.remove('visible');
      msg.innerText = '';
    });
    
    const inputs = contactForm.querySelectorAll('input, textarea');
    inputs.forEach(input => input.classList.remove('error'));
    
    const statusDiv = document.getElementById('formStatus');
    statusDiv.classList.remove('visible', 'success', 'error');
    statusDiv.innerText = '';

    let isValid = true;
    
    // Validate fields
    const fieldsToValidate = ['name', 'email', 'message'];
    
    const lang = document.documentElement.lang || 'en';
    const t = translations[lang] || translations['en'];
    
    fieldsToValidate.forEach(fieldName => {
      const field = document.getElementById(fieldName);
      if (!field.value.trim()) {
        isValid = false;
        field.classList.add('error');
        const errorDiv = document.getElementById(fieldName + '-error');
        let displayName = fieldName.charAt(0).toUpperCase() + fieldName.slice(1);
        
        // Localize field name based on current language if possible, else default to English
        if (lang === 'de') {
            if (fieldName === 'name') displayName = 'Name';
            if (fieldName === 'email') displayName = 'E-Mail';
            if (fieldName === 'message') displayName = 'Nachricht';
        } else if (lang === 'tr') {
            if (fieldName === 'name') displayName = 'İsim';
            if (fieldName === 'email') displayName = 'E-posta';
            if (fieldName === 'message') displayName = 'Mesaj';
        }
        
        errorDiv.innerText = `${t.form_required_field || "This field is required: "}${displayName}`;
        errorDiv.classList.add('visible');
      } else if (fieldName === 'email') {
        const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
        if (!emailRegex.test(field.value.trim())) {
          isValid = false;
          field.classList.add('error');
          const errorDiv = document.getElementById(fieldName + '-error');
          errorDiv.innerText = t.form_invalid_email || `Please enter a valid email address.`;
          errorDiv.classList.add('visible');
        }
      }
    });

    if (isValid) {
      const submitBtn = document.getElementById('submitBtn');
      const originalBtnText = submitBtn.innerHTML;
      
      const lang = document.documentElement.lang || 'en';
      const t = translations[lang] || translations['en'];
      
      submitBtn.innerText = t.form_sending || 'Sending...';
      submitBtn.disabled = true;

      const formData = new FormData(contactForm);

      fetch('https://formsubmit.co/ajax/info@gikstudio.com', {
        method: 'POST',
        headers: {
          'Accept': 'application/json'
        },
        body: formData
      })
      .then(response => response.json())
      .then(data => {
        if (data.success === true || data.success === "true") {
          statusDiv.innerText = t.form_success || "Thank you! Your message has been sent successfully.";
          statusDiv.classList.add('visible', 'success');
          contactForm.reset();
        } else {
          statusDiv.innerText = data.message || t.form_error || "Oops! Something went wrong, please try again.";
          statusDiv.classList.add('visible', 'error');
        }
      })
      .catch(error => {
        statusDiv.innerText = t.form_error || "Oops! Something went wrong, please check your connection and try again.";
        statusDiv.classList.add('visible', 'error');
      })
      .finally(() => {
        submitBtn.innerHTML = originalBtnText;
        submitBtn.disabled = false;
      });
    }
  });
}