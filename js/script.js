document.addEventListener('DOMContentLoaded', () => {
  // Language Switcher Logic
  const langButtons = document.querySelectorAll('.lang-btn');

  function updateLanguage(lang) {
    const texts = translations[lang];
    if (!texts) return;

    // Update active button
    langButtons.forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    // Update texts
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const attr = el.getAttribute('data-i18n-attr');

      if (translations[lang][key]) {
        if (attr === 'href') {
          el.href = translations[lang][key];
        } else {
          if (key.includes('title') || key.includes('stmt') || key.includes('desc') || key.includes('about') || key.includes('hero_headline')) {
            el.innerHTML = translations[lang][key];
          } else {
            el.textContent = translations[lang][key];
          }
        }
      }
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.dataset.i18nPlaceholder;
      if (texts[key]) {
        el.placeholder = texts[key];
      }
    });

    // Update document language
    document.documentElement.lang = lang;
    
    // Save to localStorage
    localStorage.setItem('gik_lang', lang);
  }

  const langSwitcher = document.querySelector('.lang-switcher');

  // Attach event listeners
  langButtons.forEach(btn => {
    btn.addEventListener('click', () => {
      if (btn.classList.contains('active')) {
        langSwitcher.classList.toggle('expanded');
      } else {
        updateLanguage(btn.dataset.lang);
        langSwitcher.classList.remove('expanded');
      }
    });
  });

  // Initialize language from localStorage or default to 'en'
  const savedLang = localStorage.getItem('gik_lang') || 'en';
  updateLanguage(savedLang);

  // Close switcher if clicking outside
  document.addEventListener('click', (e) => {
    if (!langSwitcher.contains(e.target)) {
      langSwitcher.classList.remove('expanded');
    }
  });

  // Smooth appearance of elements on scroll (Optional simple effect)
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.style.opacity = 1;
        entry.target.style.transform = 'translateY(0)';
      }
    });
  }, { threshold: 0.1 });

  document.querySelectorAll('.card, .hero-content, .section-header').forEach(el => {
    el.style.opacity = 0;
    el.style.transform = 'translateY(20px)';
    el.style.transition = 'opacity 0.6s ease-out, transform 0.6s ease-out';
    observer.observe(el);
  });

  // Go to top feature on signature-g
  const signatureG = document.querySelector('.signature-g');
  if (signatureG) {
    signatureG.addEventListener('click', () => {
      window.scrollTo({
        top: 0,
        behavior: 'smooth'
      });
    });
  }

  // Mobile Menu Logic
  const mobileMenuBtn = document.querySelector('.mobile-menu-btn');
  const navLinksList = document.querySelector('.nav-links');

  if (mobileMenuBtn && navLinksList) {
    mobileMenuBtn.addEventListener('click', () => {
      navLinksList.classList.toggle('active');
    });

    // Close menu when clicking a link
    navLinksList.querySelectorAll('a').forEach(link => {
      link.addEventListener('click', () => {
        navLinksList.classList.remove('active');
      });
    });
  }

  // Navbar Scroll Transparency Logic
  const navbar = document.querySelector('.navbar');
  if (navbar) {
    window.addEventListener('scroll', () => {
      // Toggle 'scrolled' class after scrolling down 50px
      if (window.scrollY > 50) {
        navbar.classList.add('scrolled');
      } else {
        navbar.classList.remove('scrolled');
      }
    });

    // Run once on load to ensure correct state if page is loaded already scrolled
    if (window.scrollY > 50) {
      navbar.classList.add('scrolled');
    }
  }
});

